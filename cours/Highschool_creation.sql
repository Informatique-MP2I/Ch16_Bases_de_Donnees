/* Highschool database */

/* Table creations */
CREATE TABLE training (
       acronym VARCHAR(20) PRIMARY KEY,
       full_name TEXT,
       apprenticeship BOOLEAN DEFAULT FALSE
);

CREATE TABLE report_card (
       id_card INT PRIMARY KEY,
       appreciation TEXT,
       mathematics INT CHECK (mathematics >= 0 AND mathematics <= 20),
       physics INT CHECK (physics >= 0 AND physics <= 20),
       computer_sci INT CHECK (computer_sci >= 0 AND computer_sci <= 20),
       eng_sci INT CHECK (eng_sci >= 0 AND eng_sci <= 20),
       french INT CHECK (french >= 0 AND french <= 20),
       english INT CHECK (english >= 0 AND english <= 20)
);

CREATE TABLE student (
       id_student INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
       /* id_student SERIAL PRIMARY KEY, */
       first_name VARCHAR(50),
       last_name VARCHAR(50),
       address VARCHAR (500),
       enrollment VARCHAR(20),
       id_card INT,
       FOREIGN KEY (enrollment) REFERENCES training(acronym),
       FOREIGN KEY (id_card) REFERENCES report_card(id_card) 
);

CREATE TABLE course (
       id_course VARCHAR(10) PRIMARY KEY,
       title VARCHAR(255),
       description TEXT
);

CREATE TABLE offer (
       training VARCHAR(50),
       course VARCHAR(10),
       PRIMARY KEY (training, course),
       FOREIGN KEY (training) REFERENCES training(acronym),
       FOREIGN KEY (course) REFERENCES course(id_course)
);

